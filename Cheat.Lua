-- –ù–∞–¥–µ—Ç—å —Å–∫–∏–Ω –Ω–∞ Part
local SkinBtn = makeButton("üëï –°–∫–∏–Ω –Ω–∞ Part", 175, Color3.fromRGB(100, 0, 150))
SkinBtn.MouseButton1Click:Connect(function()
    local part = workspace:FindFirstChild("MyPart")
    if not part or not part:IsA("BasePart") then
        warn("[–ù–ê–£–ß–ù–û] Part 'MyPart' –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        return
    end

    -- –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—ã–µ –∞–∫—Å–µ—Å—Å—É–∞—Ä—ã —Å Part
    for _, child in pairs(part:GetChildren()) do
        if child:IsA("Accessory") then
            child:Destroy()
        end
    end

    local char = player.Character or player.CharacterAdded:Wait()

    for _, accessory in pairs(char:GetChildren()) do
        if accessory:IsA("Accessory") then
            local clone = accessory:Clone()
            clone.Parent = part

            local handle = clone:FindFirstChild("Handle")
            if handle then
                -- –°–æ–∑–¥–∞—ë–º WeldConstraint –¥–ª—è –∫—Ä–µ–ø–ª–µ–Ω–∏—è –∞–∫—Å–µ—Å—Å—É–∞—Ä–∞ –∫ Part
                local weld = Instance.new("WeldConstraint")
                weld.Part0 = handle
                weld.Part1 = part
                weld.Parent = handle

                -- –ü–æ–¥–≥–æ–Ω—è–µ–º –ø–æ–∑–∏—Ü–∏—é –∞–∫—Å–µ—Å—Å—É–∞—Ä–∞ –∫ Part
                handle.CFrame = part.CFrame
            end
        end
    end
    warn("[–ù–ê–£–ß–ù–û] –°–∫–∏–Ω –æ–¥–µ—Ç –Ω–∞ Part 'MyPart'!")
end)

