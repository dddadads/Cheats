-- === üëï –û–î–ï–¢–¨ –°–ö–ò–ù –ù–ê PART ===
local SkinBtn = makeButton("üëï –°–∫–∏–Ω –Ω–∞ Part", 160, Color3.fromRGB(100, 0, 150))
SkinBtn.MouseButton1Click:Connect(function()
    local part = workspace:FindFirstChild("MyPart") -- —Ü–µ–ª–µ–≤–æ–π Part
    if not part then
        warn("[–ù–ê–£–ß–ù–û] Part 'MyPart' –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        return
    end

    local char = player.Character or player.CharacterAdded:Wait()
    for _, accessory in pairs(char:GetChildren()) do
        if accessory:IsA("Accessory") then
            local clone = accessory:Clone()
            clone.Parent = part

            local handle = clone:FindFirstChild("Handle")
            if handle then
                local weld = Instance.new("WeldConstraint")
                weld.Part0 = handle
                weld.Part1 = part
                weld.Parent = handle
                handle.CFrame = part.CFrame
            end
        end
    end
    warn("[–ù–ê–£–ß–ù–û] Part —Ç–µ–ø–µ—Ä—å –Ω–æ—Å–∏—Ç —Ç–≤–æ–π —Å–∫–∏–Ω!")
end)

